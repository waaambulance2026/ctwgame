// Cloudflare Pages Function: /api/sprite-manifest
// Builds a sprite manifest for the game.
// IMPORTANT: Cloudinary Media Library folders are NOT always the public delivery path.
// This function therefore uses public URL probing as the reliable fallback.
// It can use the Cloudinary Admin API if available, but it does NOT depend on it.

const DEFAULT_CLOUD = 'dpwlfmhia';
const CHARACTER_WIDTH = 384;
const BACKGROUND_WIDTH = 1800;
const MAX_FRAMES = 200;              // safety limit; raise later if you genuinely need more
const STOP_AFTER_MISSES = 3;         // stops after this many missing frames in a row

const CHARACTER_SPECS = {
  ax: {
    name: 'Ax',
    actions: {
      idle: { patterns: ['ax_idle_###.png', 'ax_idle#.png'] },
      walk: { patterns: ['ax_walk_###.png', 'ax_walk#.png'] },
      jump: { patterns: ['ax_jump_###.png', 'ax_jump#.png'] }
    }
  },
  pura: {
    name: 'Pura',
    actions: {
      idle: { patterns: ['pura_idle_###.png', 'pura_idle#.png'] },
      walk: { patterns: ['pura_walk_###.png', 'pura_walk#.png'] },
      jump: { patterns: ['pura_jump_###.png', 'pura_jump#.png'] }
    }
  },
  unicorn: {
    name: 'Unicorn',
    actions: {
      idle: { patterns: ['unicorn_idle_###.png', 'unicorn_idle#.png'] },
      walk: { patterns: ['unicorn_walk_###.png', 'unicorn_walk#.png', 'Uni_Walk_#.png', 'Uni_walk_#.png'] },
      jump: { patterns: ['unicorn_jump_###.png', 'unicorn_jump#.png'] }
    }
  },
  owl: {
    name: 'Owl',
    actions: {
      idle: { patterns: ['owl_idle_#.png', 'owl_idle_###.png'] },
      walk: { patterns: ['owl_walk##.png', 'owl_walk#.png'] },
      jump: { patterns: ['owl_jump_#.png', 'owl_jump_###.png'] },
      play: { patterns: ['owl_play_#.png', 'owl_play_###.png'] },
      rest: { patterns: ['owl_rest_#.png', 'owl_rest_###.png'] }
    }
  }
};

const BACKGROUND_FILES = {
  day: {
    sky: ['day_sky.png'],
    hills: ['day_hills.png'],
    ground: ['day_ground.png']
  },
  night: {
    sky: ['night_sky.png'],
    hills: ['night_hills.png'],
    ground: ['night_ground.png']
  }
};

function json(data, status = 200) {
  return new Response(JSON.stringify(data, null, 2), {
    status,
    headers: {
      'content-type': 'application/json; charset=utf-8',
      'cache-control': 'public, max-age=120'
    }
  });
}

function pad3(n) { return String(n).padStart(3, '0'); }
function pad2(n) { return String(n).padStart(2, '0'); }
function fillPattern(pattern, n) {
  return pattern
    .replace('###', pad3(n))
    .replace('##', pad2(n))
    .replace('#', String(n));
}
function encodePublicIdPath(path) {
  return String(path).split('/').map(encodeURIComponent).join('/');
}
function cloudinaryUrl(cloud, file, width) {
  // Versionless public delivery URL. This avoids the version-number problem completely.
  // If the public ID is root-level pura_idle_001, this URL works no matter what upload version it has.
  return `https://res.cloudinary.com/${cloud}/image/upload/f_auto,q_auto,w_${width}/${encodePublicIdPath(file)}`;
}
async function imageExists(url) {
  try {
    let res = await fetch(url, { method: 'HEAD', cf: { cacheTtl: 120, cacheEverything: true } });
    if (res.ok) return true;
    // Some CDNs are fussy about HEAD; try a tiny GET fallback.
    if (res.status === 405 || res.status === 403) {
      res = await fetch(url, { method: 'GET', cf: { cacheTtl: 120, cacheEverything: true } });
      return res.ok;
    }
    return false;
  } catch (_) {
    return false;
  }
}
async function firstExisting(cloud, files, width) {
  for (const file of files) {
    const url = cloudinaryUrl(cloud, file, width);
    if (await imageExists(url)) return url;
  }
  return null;
}
async function probeFrames(cloud, patterns) {
  const frames = [];
  let misses = 0;
  for (let i = 1; i <= MAX_FRAMES; i++) {
    let found = null;
    for (const pattern of patterns) {
      const file = fillPattern(pattern, i);
      const url = cloudinaryUrl(cloud, file, CHARACTER_WIDTH);
      if (await imageExists(url)) {
        found = url;
        break;
      }
    }
    if (found) {
      frames.push(found);
      misses = 0;
    } else {
      misses++;
      if (frames.length > 0 && misses >= STOP_AFTER_MISSES) break;
      // If nothing found yet, still stop fairly quickly for missing actions.
      if (frames.length === 0 && i >= STOP_AFTER_MISSES) break;
    }
  }
  return frames;
}

export async function onRequestGet(context) {
  const cloud = context.env.CLOUDINARY_CLOUD_NAME || DEFAULT_CLOUD;
  const manifest = {
    ok: true,
    generatedAt: new Date().toISOString(),
    cloudName: cloud,
    baseUrl: `https://res.cloudinary.com/${cloud}/image/upload`,
    note: 'Generated by probing public Cloudinary delivery URLs. Uses versionless f_auto,q_auto,w_384 sprite URLs so upload version numbers do not break the game.',
    backgrounds: {},
    characters: {},
    counts: {}
  };

  for (const [char, spec] of Object.entries(CHARACTER_SPECS)) {
    manifest.characters[char] = { name: spec.name, frames: {} };
    manifest.counts[char] = {};
    for (const [action, cfg] of Object.entries(spec.actions)) {
      const frames = await probeFrames(cloud, cfg.patterns);
      manifest.characters[char].frames[action] = frames;
      manifest.counts[char][action] = frames.length;
    }
  }

  for (const [theme, layers] of Object.entries(BACKGROUND_FILES)) {
    manifest.backgrounds[theme] = {};
    for (const [layer, files] of Object.entries(layers)) {
      // Try root-level first, then common folder-style fallbacks.
      const candidates = [];
      for (const file of files) {
        candidates.push(file);
        candidates.push(`ctwgame/layers/backgrounds/${file}`);
        candidates.push(`ctwgame/layers/middle-ground/${file}`);
        candidates.push(`ctwgame/layers/ground/${file}`);
      }
      manifest.backgrounds[theme][layer] = await firstExisting(cloud, candidates, BACKGROUND_WIDTH);
    }
  }

  return json(manifest);
}
